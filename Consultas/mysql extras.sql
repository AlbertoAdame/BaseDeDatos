CREATE TABLE DEPART(
	dept_no INT(10),
	dnombre VARCHAR(25),
	loc VARCHAR(25),
	CONSTRAINT pk_depart PRIMARY KEY (dept_no)

);



CREATE TABLE EMPLE(
	emp_no int(10),
	apellido VARCHAR(25),
	oficio VARCHAR(50),
	dir int(10),
	fecha_alta DATE,
	salario int(10),
	comision int(10),
	dept_no int(10),
	CONSTRAINT pk_emp_no PRIMARY KEY (emp_no),
	CONSTRAINT fk_dept_no_depart FOREIGN KEY (dept_no) REFERENCES DEPART(dept_no)

);


